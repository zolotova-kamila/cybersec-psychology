// Этот код нужно добавить в обработку входящих сообщений

// Список разрешённых ключевых слов
const ALLOWED_KEYWORDS = [
  // Главное меню
  '/start', 'меню', 'help', 'помощь',
  
  // Запись
  '/consultation', 'записаться', 'консультация', 'запись', 
  'хочу записаться', 'как записаться',
  
  // FAQ
  '/faq', 'вопросы', 'вопрос', 'частые вопросы', 'faq',
  'как работаете', 'как это работает',
  
  // Материалы
  '/materials', 'материалы', 'материал', 'чек-лист', 'чеклист',
  'презентация', 'гайд', 'бесплатно', 'скачать',
  
  // Цены
  '/price', 'цены', 'цена', 'стоимость', 'сколько стоит',
  'прайс', 'тариф', 'оплата',
  
  // Контакты
  '/contact', 'контакты', 'связаться', 'телефон', 'почта',
  'email', 'адрес', 'где находитесь', 'как вас найти',
  
  // О психологе
  '/about', 'о психологе', 'камила золотова', 'кто вы',
  'опыт', 'образование', 'специализация', 'направления',
  'чем занимаетесь', 'с чем работаете'
];

// Стандартный ответ на непонятные сообщения
const DEFAULT_RESPONSE = `Извините, не понял ваш вопрос. Передам его специалисту.

Вы можете:
• 📝 Оставить заявку на консультацию — напишите "записаться"
• ❓ Посмотреть частые вопросы — напишите "вопросы"
• 📚 Получить бесплатные материалы — напишите "материалы"
• 💰 Узнать стоимость — напишите "цены"`;

// Функция проверки сообщения
function isAllowedMessage(text) {
  if (!text) return false;
  const lowerText = text.toLowerCase().trim();
  
  return ALLOWED_KEYWORDS.some(keyword => 
    lowerText.includes(keyword.toLowerCase())
  );
}

// Функция обработки входящего сообщения
function handleIncomingMessage(messageText, userId) {
  if (isAllowedMessage(messageText)) {
    // Обрабатываем разрешённую команду
    return processAllowedCommand(messageText, userId);
  } else {
    // Отправляем стандартный ответ
    return {
      type: 'default_response',
      text: DEFAULT_RESPONSE,
      notifyAdmin: true, // Уведомить специалиста о непонятном вопросе
      userMessage: messageText,
      userId: userId
    };
  }
}

// Обработка разрешённых команд
